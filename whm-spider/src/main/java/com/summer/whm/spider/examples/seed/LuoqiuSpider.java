    public static List<StorySeed> buildStorySeed(String url) {
        try {
            System.out.println(url);
            WebClientPool webClientPool = new WebClientPool();
            final HtmlPage htmlpage = webClientPool.borrowWebClient().getPage(url);
            List<StorySeed> seedList = new ArrayList<StorySeed>();
            List<HtmlAnchor> anchorList = (List<HtmlAnchor>) htmlpage.getByXPath("//table[@class='sf-grid']/tbody/tr/td[2]/a");
            List<HtmlAnchor> categoryList = (List<HtmlAnchor>) htmlpage.getByXPath("//table[@class='sf-grid']/tbody/tr/td[1]/a");
            if (anchorList != null && anchorList.size() > 0) {
                Integer categoryId = null;
                int i = 0;
                for (HtmlAnchor htmlAnchor : anchorList) {
                    if (htmlAnchor.getAttribute("href") != null) {
                        
                        categoryId = getCategoryId(categoryList.get(i).getTextContent());
                        
                        if(categoryId != null){
                            seedList.add(new StorySeed(htmlAnchor.getTextContent(), categoryId + "", "23", htmlAnchor
                                    .getAttribute("href"), ""));
                        }
                    }
                    i++;
                }
            }
            
            return seedList;
        } catch (FailingHttpStatusCodeException e) {
            e.printStackTrace();
        } catch (MalformedURLException e) {
            e.printStackTrace();
        } catch (ElementNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }

